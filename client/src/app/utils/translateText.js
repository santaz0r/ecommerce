export function translateText(text) {
    switch (text) {
        case "women's clothing":
            return "Женская одежда";
        case "electronics":
            return "Электроника";
        case "jewelery":
            return "Украшения";
        case "men's clothing":
            return "Мужская одежда";
        case "title":
            return "Имени";
        case "price":
            return "Цене";
        case "rating.rate":
            return "Рейтингу";
    }
}
